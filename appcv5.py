# ---------------- HEADER ----------------
st.markdown('<div class="title">🚗 Car Price Prediction</div>', unsafe_allow_html=True)
st.markdown('<div class="subtitle">Smart AI-based car price estimator</div>', unsafe_allow_html=True)

# ---------------- FORM ----------------
with st.form("prediction_form"):

    col1, col2 = st.columns(2)

    with col1:
        vehicle_age = st.number_input("Vehicle Age", min_value=0, value=5, step=1)
        km_driven = st.number_input("KM Driven", min_value=1, value=50000, step=1000)
        mileage = st.number_input("Mileage", min_value=1.0, value=18.0, step=0.1)

    with col2:
        engine = st.number_input("Engine (CC)", min_value=500, max_value=5000, value=1200, step=100)
        max_power = st.number_input("Max Power", min_value=10.0, max_value=500.0, value=75.0, step=1.0)
        seats = st.number_input("Seats", min_value=2, max_value=10, value=5, step=1)

    seller_type = st.selectbox("Seller Type", ["Dealer", "Individual", "Trustmark Dealer"])
    fuel_type = st.selectbox("Fuel Type", ["Petrol", "Diesel", "CNG", "LPG", "Electric"])
    transmission = st.selectbox("Transmission", ["Manual", "Automatic"])

    submit = st.form_submit_button("Predict Price 💰")

# ---------------- PREDICTION ----------------
if submit:

    # ENCODING
    seller_Dealer = 1 if seller_type == "Dealer" else 0
    seller_Individual = 1 if seller_type == "Individual" else 0
    seller_Trustmark = 1 if seller_type == "Trustmark Dealer" else 0

    fuel_CNG = 1 if fuel_type == "CNG" else 0
    fuel_Diesel = 1 if fuel_type == "Diesel" else 0
    fuel_Electric = 1 if fuel_type == "Electric" else 0
    fuel_LPG = 1 if fuel_type == "LPG" else 0
    fuel_Petrol = 1 if fuel_type == "Petrol" else 0

    trans_Auto = 1 if transmission == "Automatic" else 0
    trans_Manual = 1 if transmission == "Manual" else 0

    # INPUT ARRAY
    input_data = np.array([[
        vehicle_age, km_driven, mileage, engine, max_power, seats,
        seller_Dealer, seller_Individual, seller_Trustmark,
        fuel_CNG, fuel_Diesel, fuel_Electric, fuel_LPG, fuel_Petrol,
        trans_Auto, trans_Manual
    ]])

    prediction = model.predict(input_data)

    # OUTPUT
    st.markdown(f"""
    <div class="success-box">
        💰 Estimated Price: ₹ {prediction[0]:,.2f}
    </div>
    """, unsafe_allow_html=True)